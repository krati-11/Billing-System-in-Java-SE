    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        try
        {
            tm.setRowCount(0);
            String orderId = cbSelectId.getSelectedItem().toString();
            order = OrdersDAO.getOrderById(orderId);
            if(order.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No order found!");
                return;
            }
            for (Map.Entry<String, Integer> entry : order.entrySet()) {
            String pid = entry.getKey();
            int quan = entry.getValue();
            ProductsPojo p = ProductsDAO.getProductDetails(pid);
            Object [] rows = new Object[7];
            rows[0] = pid;
            rows[1] = p.getProductName();
            rows[2] = p.getProductCompany();
            rows[3] = p.getProductPrice();
            rows[4] = p.getOurPrice();
            rows[5] = quan;
            rows[6] = p.getTax();
            tm.addRow(rows);
        }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error!");
            ex.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnShowActionPerformed

