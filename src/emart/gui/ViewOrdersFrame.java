    private void loadAllOrders() {
        try
        {
            ArrayList<String> ids;
            if(UserProfile.getUsertype().equals("Manager"))
             ids = OrdersDAO.getAllOrders();
            else
             ids = OrdersDAO.getAllOrdersForRecep(UserProfile.getUserid());
            if(ids.isEmpty())
            {
             JOptionPane.showMessageDialog(null, "No orders present!");
             return;
            }
            btnShow.setEnabled(true);
            DefaultComboBoxModel dbm = (DefaultComboBoxModel) cbSelectId.getModel();
            for(String id : ids)
            {
                if(dbm.getIndexOf(id) == -1)
                  cbSelectId.addItem(id);
            }
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error!");
            ex.printStackTrace();
            return;
        }
    }
