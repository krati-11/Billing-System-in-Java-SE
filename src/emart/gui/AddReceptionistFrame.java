    private void loadReceptionistDetails() {
        try
        {
            jcEmpId.removeAllItems();
            receptionist = ReceptionistDAO.getNonRegisteredReceptionist();
            if(receptionist.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No un-registered receptionists left!");
                btnAdd.setEnabled(false);
                return;
            }
            btnAdd.setEnabled(true);
            Set<String> keys = receptionist.keySet();
            Iterator<String> it = keys.iterator();
            //jcEmpId.removeAllItems();
            while(it.hasNext())
            {
                jcEmpId.addItem(it.next());
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error!");
            ex.printStackTrace();
            return;
        }
     }

    private boolean isPasswordMatching(String spwd, String srepwd) {
        return spwd.equals(srepwd);
    }

