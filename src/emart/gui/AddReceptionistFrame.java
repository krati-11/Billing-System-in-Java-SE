    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int result = validateInputs();
        if(result == 0)
        {
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long!");
            return;
        }
        if(result == -1)
        {
            JOptionPane.showMessageDialog(null, "User Id cannot be left blank!");
            return;
        }
        char []pwd = txtPassword.getPassword();
        char[] repwd = txtRePassword.getPassword();
        String spwd = String.valueOf(pwd);
        String srepwd = String.valueOf(repwd);
        if(isPasswordMatching(spwd,srepwd) == false)
        {
            JOptionPane.showMessageDialog(null, "Password does not match with re-password!");
            return;
        }
        try
        {
            String empid = jcEmpId.getSelectedItem().toString();
            String userid = txtUserId.getText().trim();
            String username = txtEmpName.getText().trim();
            UserPojo user = new UserPojo();
            user.setEmpid(empid);
            user.setPassword(spwd);
            user.setUserid(userid);
            user.setUsername(username);
            user.setUsertype("Receptionist");
            boolean status = ReceptionistDAO.addReceptionist(user);
            if(status)
            {
                JOptionPane.showMessageDialog(null, "Receptionist registered successfully!");
                clearText();
                loadReceptionistDetails();
                return;
            }
            JOptionPane.showMessageDialog(null, "Receptionist could not be registered! Please try again!");
            
        }
        catch(SQLException ex)
        {
             JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
             ex.printStackTrace();
             return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

