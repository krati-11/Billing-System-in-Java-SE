    public boolean validateInputs()
    {
        char[] pwd = txtPassword.getPassword();
        char[] rpwd = txtRePassword.getPassword();
        if(pwd.length < 4 || rpwd.length < 4)
        {
             JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long","Error!",JOptionPane.ERROR_MESSAGE);
             return false;
        }
        String spwd = String.valueOf(pwd);
        String srpwd = String.valueOf(rpwd);
        password = spwd;
        if(!spwd.equals(srpwd))
        {
            JOptionPane.showMessageDialog(null, "Passwords do not match!","Error!",JOptionPane.ERROR_MESSAGE);
             return false;
        }
        return true;
    }

