    private void setIds() {
        try
        {
            List<String> idList = EmployeeDAO.getAllEmpId();
            cbEmpId.removeAllItems();
            for(String s: idList)
            {
                cbEmpId.addItem(s);
            }
        }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Sorry couldn't generate Employee Ids!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    
     private boolean validateInputs()
    {
        return !(txtEmpName.getText().isEmpty() || txtSalary.getText().isEmpty());
    }
     
     private void clearText() {
        txtEmpName.setText("");
        txtSalary.setText("");
        cbJob.setSelectedIndex(0); 
        cbEmpId.removeAllItems();
    }
