    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       if(!validateInputs())
       {
            JOptionPane.showMessageDialog(null, "Please fill all the information!","Incomplete Data!",JOptionPane.ERROR_MESSAGE);
            return;
       }
       try
       {
           EmployeePojo emp = new EmployeePojo();
           emp.setEmpid(cbEmpId.getSelectedItem().toString());
           emp.setEmpname(txtEmpName.getText());
           emp.setJob(cbJob.getSelectedItem().toString());
           emp.setSalary(Double.parseDouble(txtSalary.getText()));
           if(!EmployeeDAO.UpdateEmployee(emp))
           {
               JOptionPane.showMessageDialog(null, "Could not update data!Try again!","Error!",JOptionPane.ERROR_MESSAGE);
               return;
           }
           JOptionPane.showMessageDialog(null, "Employee info updated successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
           clearText();
           setIds();
       }
       catch(SQLException ex)
        {
             JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
             ex.printStackTrace();
             return;
        }
        catch(NumberFormatException ex)
        {
             JOptionPane.showMessageDialog(null, "Please fill appropriate information!","Invalid Data!",JOptionPane.ERROR_MESSAGE);
             return;
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void cbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEmpIdActionPerformed
        try
        {
            String id = cbEmpId.getSelectedItem().toString();
            EmployeePojo emp = EmployeeDAO.getEmployeeById(id);
            if(emp == null)
            {
                JOptionPane.showMessageDialog(null, "Some error occured while retrieving information!","Error!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            txtEmpName.setText(emp.getEmpname());
            txtSalary.setText(String.valueOf(emp.getSalary()));
            cbJob.setSelectedItem(emp.getJob());
        }
        catch(SQLException ex)
        {
             JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
             ex.printStackTrace();
             return;
        }    
    }//GEN-LAST:event_cbEmpIdActionPerformed

